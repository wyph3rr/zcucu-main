local function GetMaterialNameFromID(id)
    if id == MAT_METAL then return "metal"
    elseif id == MAT_GRATE then return "vent"
    elseif id == MAT_DIRT then return "dirt"
	elseif id == MAT_SAND then return "sand"
    elseif id == MAT_WOOD then return "wood"
    elseif id == MAT_SLOSH then return "mud"
	elseif id == MAT_GLASS then return "glass"
    elseif id == MAT_GRASS then return "grass"
    elseif id == MAT_GRAVEL then return "gravel"
	elseif id == MAT_TILE then return "tile"
    elseif id == MAT_SNOW then return "snow"
    else return "default"
    end
end

local function GetMaterialType(ent, pos)
    local baseTrace = util.TraceLine({
        start = pos + Vector(0, 0, 10),
        endpos = pos - Vector(0, 0, 20),
        filter = ent
    })

    if baseTrace.Hit and baseTrace.MatType and baseTrace.MatType ~= MAT_DEFAULT then
        return GetMaterialNameFromID(baseTrace.MatType)
    end

    -- fallback: scan around the player’s feet
    local directions = {
        Vector(20, 0, -10), Vector(-20, 0, -10),
        Vector(0, 20, -10), Vector(0, -20, -10),
        Vector(15, 15, -10), Vector(-15, -15, -10),
        Vector(15, -15, -10), Vector(-15, 15, -10)
    }

    for _, dir in ipairs(directions) do
        local tr = util.TraceLine({
            start = pos + Vector(0, 0, 5),
            endpos = pos + dir,
            filter = ent
        })

        if tr.Hit and tr.MatType and tr.MatType ~= MAT_DEFAULT then
            return GetMaterialNameFromID(tr.MatType)
        end
    end

    return "default"
end

hook.Add("OnPlayerHitGround", "CustomLandingSounds", function(ply, inWater, onFloater, speed)
    if onFloater or not ply:IsOnGround() then return end
    if speed < 200 then return end

    local curTime = CurTime()
    if ply.NextLandingSound and ply.NextLandingSound > curTime then return end
    ply.NextLandingSound = curTime + 0.15

    local landingType = speed > 400 and "hard" or "light"
    local matType

    if inWater then
        matType = "water"
    else
        matType = GetMaterialType(ply, ply:GetPos())
    end

    local soundTable = LANDING_SOUNDS[matType] or LANDING_SOUNDS["default"]
    local sounds = soundTable[landingType]

    if not sounds or #sounds == 0 then return end

    local soundPath = sounds[math.random(#sounds)]
    sound.Play(soundPath, ply:GetPos(), 75, 100, 1)
end)
